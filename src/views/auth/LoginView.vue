<script setup>
import { ref } from "vue";
import { useAuthStore } from "@/stores/auth";
import { useRouter } from "vue-router";
import BaseAlertError from "@/components/Alert/BaseAlertError.vue";
import BasePasswordInput from "@/components/input/BasePasswordInput.vue";
import BaseLoading from "@/components/BaseLoading.vue";
import { watch } from "vue";

const authStore = useAuthStore();
const { userLogin } = authStore;

const loading = ref(false);
const error = ref(false);
const router = useRouter();
const disabled = ref(true);

const payload = ref({
  email: "",
  senha: "",
});

const handleSubmit = async () => {
  loading.value = true;
  const login = await userLogin(payload.value);

  if (login) {
    localStorage.setItem("token-auth", login.token);
    localStorage.setItem("user_id", login.user.user_id);
    localStorage.setItem("user_type_id", login.user.user_type_id);

    router.push({ path: "/course" });
  } else {
    error.value = true;

    setTimeout(() => {
      error.value = false;
    }, 3000);
  }
  loading.value = false;
};

const handleKeyPress = (event) => {
  if (event.key === "Enter" && !disabled.value) {
    handleSubmit();
  }
};

watch(payload.value, () => {
  if (payload.value.email.length > 1 && payload.value.senha.length > 1) {
    disabled.value = false;
  } else {
    disabled.value = true;
  }
});
</script>

<template>
  <div class="flex login-view-container">
    <div class="form-container">
      <div class="form flex column">
        <h1 class="poppins-medium">Login</h1>
        <h2 class="poppins-medium">Seja bem-vindo ao Smart Edu!</h2>

        <div class="flex column input-container">
          <label>Email</label>
          <input
            type="email"
            placeholder="Insira seu email"
            :value="payload.email"
            @input="(ev) => (payload.email = ev.target.value)"
            @keydown="handleKeyPress"
          />
        </div>
        <div class="flex column input-container">
          <BasePasswordInput
            label="Senha"
            v-model="payload.senha"
            @keydown="handleKeyPress"
          />
        </div>
        <div class="flex input-container w-100">
          <RouterLink class="password-span-text w-100" to="/register"
            >Esqueceu a senha?</RouterLink
          >
        </div>
        <button
          class="button"
          @click="handleSubmit"
          :class="disabled === true ? 'disabled' : ''"
          :disabled="disabled"
        >
          <span v-if="!loading">Enviar</span>
          <div
            v-else
            style="
              width: 100%;
              display: flex;
              align-items: center;
              justify-content: center;
            "
          >
            <svg
              xmlns="http://www.w3.org/2000/svg"
              viewBox="0 0 100 100"
              preserveAspectRatio="xMidYMid"
              width="28"
              height="28"
              style="
                shape-rendering: auto;
                display: block;
                background: rgba(255, 255, 255, 0);
              "
              xmlns:xlink="http://www.w3.org/1999/xlink"
            >
              <g>
                <circle
                  stroke-dasharray="164.93361431346415 56.97787143782138"
                  r="35"
                  stroke-width="10"
                  stroke="#fff"
                  fill="none"
                  cy="50"
                  cx="50"
                >
                  <animateTransform
                    keyTimes="0;1"
                    values="0 50 50;360 50 50"
                    dur="1s"
                    repeatCount="indefinite"
                    type="rotate"
                    attributeName="transform"
                  ></animateTransform>
                </circle>
                <g></g>
              </g>
              <!-- [ldio] generated by https://loading.io -->
            </svg>
          </div>
        </button>
        <span>
          Possui uma instituição de ensino?
          <RouterLink class="login-span-text" to="/register"
            >Cadastre agora</RouterLink
          >
        </span>
        <img
          class="logo"
          src="../../assets/img/png/logo-1.png"
          alt=""
          style="width: 110px; margin: auto"
        />
        <BaseAlertError
          v-if="error"
          type="error"
          text="Email ou senha incorretos"
        />
      </div>
    </div>
    <div class="theme">
      <img src="../../assets/img/png/faculdade-formatura.jpg" alt="theme" />
    </div>
  </div>
</template>

